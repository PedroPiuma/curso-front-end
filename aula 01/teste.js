// AND (&&)
console.log(true && false);

// Tabela da AND
/*
true && true   => true
true && false  => false
false && false => true
*/

// OR (||)
console.log(true || false);
// Tabela da OR
/*
true || true   => true
true || false  => true
false || false => false
*/

// NOT (!)
// Tabela do NOT
// ! true => false
// ! false => true
console.log(!true);
console.log(!false);

